// Arrays
const fruits = ['banana', 'apple', 'orange', 'pear']
console.log(fruits)

// grab the first index
console.log(fruits[0])

// Array methods (push, slice, indexOf, length)
fruits.push('grapes')

// Array slice starts from 0 inclusive and up to 2
console.log(fruits.slice(0,2))

console.log(fruits.indexOf('orange'))

console.log(fruits.length)